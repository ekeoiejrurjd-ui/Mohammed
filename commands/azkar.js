module.exports = async ({ text, reply, msg }) => {
  if (text === 'اذكار' || text === '🕌 الأذكار اليومية') {
    const index = 0;
    await reply(`📿 *الذكر رقم ${index + 1} من ${adhkar.length}:*\n\n${adhkar[index]}`, [
      { id: `adhkar:${index + 1}`, text: 'التالي' },
      { id: 'exit', text: 'خروج' }
    ]);
  }

  if (text.startsWith('adhkar:')) {
    const index = parseInt(text.split(':')[1]);
    if (isNaN(index) || index < 0 || index >= adhkar.length) return;

    const buttons = [];
    if (index > 0) buttons.push({ id: `adhkar:${index - 1}`, text: 'السابق' });
    if (index < adhkar.length - 1) buttons.push({ id: `adhkar:${index + 1}`, text: 'التالي' });
    buttons.push({ id: 'exit', text: 'خروج' });

    await reply(`📿 *الذكر رقم ${index + 1} من ${adhkar.length}:*\n\n${adhkar[index]}`, buttons);
  }

  if (text === 'exit') {
    await reply('✅ تم الخروج من قسم الأذكار.');
  }
};
